    private void VerificarInicio(String usuario, String contrasenia)
    {
        CreaUsuarioSeguridad();
        if((user1.getUser().equals(usuario)) && (user1.getContrasenia().equals(contrasenia)))
        {
            JOptionPane.showMessageDialog(null, "Inicio correcto", "ACEPTADO", JOptionPane.DEFAULT_OPTION);
            new JFInicio().setVisible(true);
            this.setVisible(false);
            dispose();
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Inicio incorrecto, revise usuario o contraseña", "DENEGADO", JOptionPane.ERROR_MESSAGE);
        }
    }
    
